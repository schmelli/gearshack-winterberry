/**
 * Types for Community RAG (Retrieval-Augmented Generation)
 * Feature: Community-RAG Integration (Vorschlag 15)
 *
 * Defines types for indexing community content (bulletin board posts,
 * replies, shakedowns) into vector embeddings for semantic search.
 */

// ============================================================================
// Source Content Types
// ============================================================================

export type CommunitySourceType =
  | 'bulletin_post'
  | 'bulletin_reply'
  | 'shakedown'
  | 'shakedown_feedback';

export interface CommunityChunk {
  /** Unique chunk ID (generated by DB) */
  id?: string;
  /** Type of source content */
  source_type: CommunitySourceType;
  /** ID of the source record */
  source_id: string;
  /** The text content of this chunk */
  chunk_text: string;
  /** Index within the source (0 for single-chunk posts) */
  chunk_index: number;
  /** Author of the original content */
  author_id: string | null;
  /** Tags from the post (e.g., ['gear_advice', 'question']) */
  tags: string[];
  /** Extracted gear/product names mentioned in the content */
  gear_names: string[];
  /** Extracted brand names mentioned in the content */
  brand_names: string[];
  /** When the source content was created */
  source_created_at: string | null;
  /** Denormalized reply count from the source post (engagement/quality signal) */
  reply_count: number;
}

// ============================================================================
// Search Types
// ============================================================================

export interface CommunitySearchResult {
  id: string;
  source_type: CommunitySourceType;
  source_id: string;
  chunk_text: string;
  tags: string[];
  gear_names: string[];
  brand_names: string[];
  author_id: string | null;
  source_created_at: string | null;
  similarity: number;
}

export interface CommunitySearchOptions {
  /** Maximum number of results to return */
  topK?: number;
  /** Minimum similarity threshold (0-1, default 0.65) */
  threshold?: number;
  /** Filter by source type */
  sourceType?: CommunitySourceType;
  /** Filter by tags (returns results matching ANY of the given tags) */
  tags?: string[];
  // Quality filters (Vorschlag 6 — Hybrid RAG)
  /** Minimum reply count as engagement/quality signal */
  minReplies?: number;
  /** Maximum age in months — excludes content older than this */
  maxAgeMonths?: number;
  /** Exclude posts with 0 replies (shorthand for minReplies: 1) */
  excludeNoEngagement?: boolean;
}

// ============================================================================
// Indexing Types
// ============================================================================

export interface IndexingResult {
  /** Number of chunks successfully indexed */
  indexed: number;
  /** Number of chunks that failed */
  failed: number;
  /** Number of chunks skipped (already up to date) */
  skipped: number;
  /** Error messages for failed chunks */
  errors: string[];
}

export interface BulletinPostForIndexing {
  id: string;
  content: string;
  tag: string | null;
  author_id: string;
  created_at: string;
  author_name?: string;
  /**
   * Engagement count from the source post. Required — callers must provide
   * the current reply_count to ensure the quality filter reflects actual engagement.
   * Use `0` when the value is unknown rather than omitting the field.
   */
  reply_count: number;
}

export interface BulletinReplyForIndexing {
  id: string;
  post_id: string;
  content: string;
  author_id: string;
  created_at: string;
  author_name?: string;
}

